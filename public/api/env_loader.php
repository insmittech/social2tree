<?php
// Function to load .env file if getenv() doesn't work (e.g. some shared hosts)
function loadEnv($path) {
    if (!file_exists($path)) {
        return;
    }

    $lines = file($path, FILE_IGNORE_NEW_LINES | FILE_SKIP_EMPTY_LINES);
    foreach ($lines as $line) {
        if (strpos(trim($line), '#') === 0) {
            continue;
        }

        list($name, $value) = explode('=', $line, 2);
        $name = trim($name);
        $value = trim($value);

        if (!array_key_exists($name, $_SERVER) && !array_key_exists($name, $_ENV)) {
            putenv(sprintf('%s=%s', $name, $value));
            $_ENV[$name] = $value;
            $_SERVER[$name] = $value;
        }
    }
}

// Try to load .env from parent directory (standard for security)
loadEnv(__DIR__ . '/../../.env'); 
// Fallback: try loading from current directory if parent fails (less secure)
loadEnv(__DIR__ . '/.env'); 
?>
